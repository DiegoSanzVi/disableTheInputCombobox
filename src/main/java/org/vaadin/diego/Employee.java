package org.vaadin.diego;

public class Employee {
    private String first;
    private String last;

    public Employee() {

    }

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public void setLast(String last) {
        this.last = last;
    }

    public String getLast() {
        return last;
    }
}
